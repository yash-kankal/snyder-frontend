export function findFranchiseBySlug(slug) {
  return FRANCHISES.find(f => f.slug === slug)
}

export function findFranchiseByCollectionId(id) {
  return FRANCHISES.find(f => f.collectionId === Number(id))
}

// Returns the matching curated franchise (if any) for a movie on the detail page
export function findFranchiseForMovie(movieId, collectionId) {
  const numId = Number(movieId)
  const byIds = FRANCHISES.find(f => f.type === 'ids' && f.movieIds.includes(numId))
  if (byIds) return byIds
  if (collectionId) return findFranchiseByCollectionId(collectionId)
  return null
}
